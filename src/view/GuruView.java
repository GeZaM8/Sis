    public void loadData() {
        tableSiswa.setModel(dao.getModelAllGuru());
        scrSiswa.setViewportView(tableSiswa);
    }
    
    public void tampilData(String nis) {
        Guru guru = dao.getGuruByNik(nis);
        inputNik.setText(guru.getNik());
        inputNama.setText(guru.getNama());
        inputTmpLahir.setText(guru.getTmpLahir());
        inputTglLahir.setDate(guru.getTglLahir());
        inputAlamat.setText(guru.getAlamat());
        inputTelepon.setText(guru.getTelp());
        inputUser.setText(guru.getUser());
        tampilGambar(guru.getFoto());
        
        if ("L".equals(guru.getJenkel())) inputRBLaki.setSelected(true);
        else if ("P".equals(guru.getJenkel())) inputRBPerempuan.setSelected(true);
    }
    
    public void tampilGambar(String file) {
        try {
            BufferedImage myImage = ImageIO.read(new File(file));
            Image dimg = myImage.getScaledInstance(inputFoto.getWidth(), inputFoto.getHeight(), Image.SCALE_SMOOTH);
            inputFoto.setIcon(new ImageIcon(dimg));
        } catch (IOException | NullPointerException e) {
            namaFile = "";
            inputFoto.setIcon(null);
        }
    }
    
    public void removeInput() {
        for (JTextField textField : textFields) {
            textField.setText("");
        }
        inputAlamat.setText("");
        inputTglLahir.setDate(null);
        inputRBLaki.setSelected(false);
        inputRBPerempuan.setSelected(false);
        
        groupJenkel.clearSelection();        
        
        nikGuru = "";
        
        inputFoto.setIcon(null);
    }
    
    public boolean checkFill() {
        for (JTextField textField : textFields) {
            if (textField.getText().isEmpty())
            return false;
        }
        if (inputAlamat.getText().isEmpty())
            return false;
        if (inputFoto.getIcon().equals(""))
            return false;
        if (inputRBLaki.isSelected() || inputRBPerempuan.isSelected())
            return true;
        
        return true;
    }
    
    public int confirm(String text, String title) {
        return JOptionPane.showOptionDialog(null, text, title, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
    }

