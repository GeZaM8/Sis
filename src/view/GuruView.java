    private void btnSaveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMousePressed
        if (checkFill() == false) {
            JOptionPane.showMessageDialog(this, "Anda belum menginput semuanya");
            return;
        }

        if (confirm("Data akan disimpan", "Simpan Data") == JOptionPane.NO_OPTION) return;

        String jenkel;
        jenkel = (inputRBLaki.isSelected()) ? "L" : (inputRBPerempuan.isSelected()) ? "P" : "";

        guru = new Guru(
                inputNik.getText().trim(), 
                inputNama.getText().trim(), 
                inputTmpLahir.getText().trim(), 
                new Date(inputTglLahir.getDate().getTime()), 
                inputAlamat.getText().trim(), 
                jenkel, 
                inputTelepon.getText().trim(), 
                inputUser.getText().trim(), 
                namaFile);

        dao.saveGuru(guru);

        removeInput();
        loadData();
    }//GEN-LAST:event_btnSaveMousePressed

    private void tableSiswaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSiswaMousePressed
        int SelectedRow = tableSiswa.rowAtPoint(evt.getPoint());

        if (SelectedRow != -1) {
            nikGuru = (String) tableSiswa.getValueAt(SelectedRow, 0);
            tampilData(nikGuru);
        }
    }//GEN-LAST:event_tableSiswaMousePressed

    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(
            System.getProperty("user.home")));

        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.jpeg", ".jpg", ".png");
        file.addChoosableFileFilter(filter);
        int res = file.showOpenDialog(null);
        if (res == JFileChooser.APPROVE_OPTION) {
            File selFile = file.getSelectedFile();
            namaFile = selFile.getAbsolutePath();
            System.out.println(namaFile);
            if (namaFile == null || "".equals(namaFile)) {
                namaFile = null;
            } else {
                tampilGambar(namaFile);
            }
        }
    }//GEN-LAST:event_btnUploadActionPerformed

