    private void btnSaveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMousePressed
        if (checkFill() == false) {
            JOptionPane.showMessageDialog(this, "Anda belum menginput semuanya");
            return;
        }
        
        if (confirm("Data akan disimpan", "Simpan Data") == JOptionPane.NO_OPTION) return;

        cabeka = new Cabeka();
        cabeka.setNis(inputInduk.getText());
        cabeka.setTgl(new Date(inputTgl.getDate().getTime()));
        cabeka.setNik(inputPengampu.getText());
        cabeka.setPermasalahan(inputPermasalahan.getText());
        cabeka.setSolusi(inputSolusi.getText());
        String kehadiran = "";
        switch (cbKehadiran.getSelectedItem().toString()) {
            case "Sakit":
                kehadiran = "S";
                break;
            case "Izin":
                kehadiran = "I";
                break;
            case "Alpha":
                kehadiran = "A";
                break;
        }
        cabeka.setKehadiran(kehadiran);
        
        dao.saveCabeka(cabeka);
        
        removeInput();
        loadData();
    }//GEN-LAST:event_btnSaveMousePressed

    private void tableCabekaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCabekaMousePressed
        int SelectedRow = tableCabeka.rowAtPoint(evt.getPoint());
        
        if (SelectedRow != -1) {
            String nis = tableCabeka.getValueAt(SelectedRow, 0).toString();
            String tgl = sdf.format(tableCabeka.getValueAt(SelectedRow, 2));
            loadInput(nis, Date.valueOf(tgl));
        }
    }//GEN-LAST:event_tableCabekaMousePressed

