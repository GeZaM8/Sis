    private void loadData() {
        Date tgl = null;
        if (inputTgl.getDate() == null) {
            tgl = null;
        } else {
            tgl = new Date(inputTgl.getDate().getTime());
        }
        try {
            tableCabeka.setModel(dao.getModelCabeka(inputInduk.getText(), tgl));
        }
        catch (NullPointerException e) {
            tableCabeka.setModel(dao.getModelCabeka("", new Date(0)));
        }
        scrCabeka.setViewportView(tableCabeka);
    }
    
    private void loadInput(String nis, Date tgl) {
        cabeka = dao.getCabeka(nis, tgl);
        
        inputInduk.setText(cabeka.getNis());
        inputPengampu.setText(cabeka.getNik());
        inputTgl.setDate(cabeka.getTgl());
        inputPermasalahan.setText(cabeka.getPermasalahan());
        inputSolusi.setText(cabeka.getSolusi());
        
        cbKehadiran.setSelectedItem(cabeka.getKehadiran());
        
        tfNamaSiswa.setText(cabeka.getSiswa().getNama());
        tfNamaPengampu.setText(cabeka.getGuru().getNama());
    }
    
    private void removeInput() {
        for (JTextField a : textFields) {
            a.setText("");
        }
        for (JTextArea a : textAreas) {
            a.setText("");
        }
        for (JDateChooser a : dateChoosers) {
            a.setDate(null);
        }
        cbKehadiran.setSelectedIndex(0);
    }
    
    private boolean checkFill() {
        for (JTextField a : textFields) {
            if (a.getText().isEmpty()) return false;
        }
        for (JTextArea a : textAreas) {
            if (a.getText().isEmpty()) return false;
        }
        for (JDateChooser a : dateChoosers) {
            if (a.getDate() == null) return false;
        }
        if (cbKehadiran.getSelectedItem() == null) return false;
        return true;
    }
    
